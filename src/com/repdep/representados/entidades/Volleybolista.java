package com.repdep.representados.entidades;

public class Volleybolista extends Persona{
	private int bloqueos;
	private int aces;
	private int remates;
	public int getBloqueos() {
		return bloqueos;
	}
	public void setBloqueos(int bloqueos) {
		this.bloqueos = bloqueos;
	}
	public int getAces() {
		return aces;
	}
	public void setAces(int aces) {
		this.aces = aces;
	}
	public int getRemates() {
		return remates;
	}
	public void setRemates(int remates) {
		this.remates = remates;
	}

	
}
