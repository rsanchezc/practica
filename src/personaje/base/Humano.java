package personaje.base;

public abstract class Humano {
    int ataque;
    int defensa;
    int vida;
    int magia;
    
    public int getAtaque(){
        return ataque;
    }
    
    public int getDefensa(){
        return defensa;
    }
    
    public int getVida(){
        return vida;
    }
    
    public int getMagia(){
        return magia;
    }
       
    
}
